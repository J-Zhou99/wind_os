#include "wind_types.h"
#include "wind_comman.h"
//把字符串转换成带符号整数
s32_t wind_convert_str2s32_t(s8_t *str)
{
    bool_t flag = 0;
    u8_t i = 0;
    s32_t num = 0;
    if(str[0] == '-')
    {
        flag = B_TRUE;
        i = 1;
    }
    while(1)
    {
        if(str[i] <= '9' && str[i] >= '0')
        {
            num = num * 10 + str[i] - '0';
            i ++;
        }
        else
            break;
    }
    if(flag)
        num = - num;
    return num;
}

//把字符串转换成无符号整数
u32_t wind_convert_str2u32_t(s8_t *str)
{
    u32_t num = 0;
    u8_t i = 0;
    while(1)
    {
        if(str[i] <= '9' && str[i] >= '0')
        {
            num = num * 10 + str[i] - '0';
            i ++;
        }
        else
            break;
    }
    return num;
}

